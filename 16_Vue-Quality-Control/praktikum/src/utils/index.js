export const penjumlahan = (variableSatu, variableDua) => {
    return variableSatu + variableDua
};

export const pengurangan = (a, b) => {
    return a - b
};

export const pembagian = (c, d) => {
    return c / d
};

export const perkalian = (e, f) => {
    return e * f
};

export const rataRata = (g, h, i, j, k) => {
    return (g+h+i+j)/k
};

export const sisaBagi = (l, m) => {
    return l % m
};

export const luasPersegi = (n, o) => {
    return n*o
};

export const luasLingkaran = (p, q) => {
    return p*q*3.14
};

export const luasKubus = (r, s, t) => {
    return r*s*t
};

export const volumeKubus = (u, v, w) => {
    return u*v*w
};